    private void jbtGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtGuardarActionPerformed
        
        boolean validacion = validar();
        if(validacion==false){
            return;
        }
        
        ProductoDao productoDao = new ProductoDao();
        ProductoModel producto = new ProductoModel();
        String codigoBarras = jtxCodigoBarras.getText();
        String nombre = jtxNombreProducto.getText();
        String strPrecio = jtxPrecio.getText();
        double precio = Double.parseDouble(strPrecio);
        
        producto.setCodigoBarras(codigoBarras);
        producto.setNombre(nombre);
        producto.setPrecio(precio);
        
        if(tipoOperacion==TipoOperacionEnum.AGREGAR)
        {
            productoDao.agregar(producto);
        }else if(tipoOperacion==TipoOperacionEnum.ACTUALIZAR){
            productoDao.actualizar(producto);
        }
        this.setVisible(false);
        this.dispose();
    }//GEN-LAST:event_jbtGuardarActionPerformed

