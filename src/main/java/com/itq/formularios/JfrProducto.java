    public void setTipoOperacion(TipoOperacionEnum tipoOperacion) {
        this.tipoOperacion = tipoOperacion;
        switch(this.tipoOperacion)
        {
            case ACTUALIZAR:
                jtxCodigoBarras.setEditable(false);
                break;
            case AGREGAR:
                jtxCodigoBarras.setEditable(true);
                break;
            default:
        }
    }
    
    public void cargar(String codigoBarras){
        ProductoDao productoDao = new ProductoDao();
        List<ProductoModel> productos = productoDao.buscar(codigoBarras);
        ProductoModel producto = productos.get(0);
        jtxCodigoBarras.setText(producto.getCodigoBarras());
        jtxNombreProducto.setText(producto.getNombre());
        double precio = producto.getPrecio();
        jtxPrecio.setText(String.valueOf(precio));
    }
    
    private boolean validar(){
        if(jtxCodigoBarras.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this,"El codigo de barras es requerida");
            return false;
        }
        if(jtxNombreProducto.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this,"El nombre del producto es requerida");
            return false;
        }
        String strPrecio = jtxPrecio.getText();
        if(strPrecio.trim().equals("")){
            JOptionPane.showMessageDialog(this,"El precio es requerida");
            return false;
        }
        
        boolean esNumero = strPrecio.matches("-?\\d+(\\.\\d+)?");        
        if(esNumero==false){
            JOptionPane.showMessageDialog(this,"Ingrese un numero valido para el precio");
            return false;
        }
        
        return true;
    }
